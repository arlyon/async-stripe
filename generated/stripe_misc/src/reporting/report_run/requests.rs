
/// Retrieves the details of an existing Report Run.
pub fn retrieve(
    client: &stripe::Client,
    report_run: &stripe_misc::reporting::report_run::ReportingReportRunId,
    params: RetrieveReportRun,
) -> stripe::Response<stripe_misc::reporting::report_run::ReportRun> {
    client
        .get_query(&format!("/reporting/report_runs/{report_run}", report_run = report_run), params)
}
/// Creates a new object and begin running the report.
